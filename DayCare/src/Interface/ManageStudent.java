    private void viewImmuBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewImmuBtnActionPerformed
        int row = studentTable.getSelectedRow();
        if(row<0) {
             JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for(Student stu: daycare.getStudents()){
            if(stu.getId().equals(studentTable.getValueAt(row, 2))){
                ImmunizationRecord ir = new ImmunizationRecord(userProcessContainer, stu, daycare);
                userProcessContainer.add("ImmunizationRecord", ir);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                break;
            }
        }
       
        
    }//GEN-LAST:event_viewImmuBtnActionPerformed

    private void deleteStuBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStuBtnActionPerformed
        int row = studentTable.getSelectedRow();
        
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for(Student stu: daycare.getStudents()){
            if(stu.getId() == studentTable.getValueAt(row, 2) && stu.getFirstname() == studentTable.getValueAt(row, 0)){
                daycare.deleteStudent(stu);
                JOptionPane.showMessageDialog(null, "Student deleted successfully.");
                break;
            }
                
            
        }
       
        refreshTable();
    }//GEN-LAST:event_deleteStuBtnActionPerformed


