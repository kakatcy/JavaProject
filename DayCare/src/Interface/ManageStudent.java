    public void refreshTable() {
        int rowCount = studentTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)studentTable.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Student s : daycare.getStudents()) {
            Object row[] = new Object[6];
            row[0] = s.getFirstname();
            row[1] = s.getLastname();
            row[2] = s.getId();
            row[3] = s.getAge();
            SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");
            row[4] = fm.format(s.getRegistrationDate());
            
            Date anniversary = s.getRegistrationDate();
            Calendar myCal = Calendar.getInstance();
            myCal.setTime(anniversary);    
            myCal.add(Calendar.YEAR, +1);    
            anniversary = myCal.getTime();
            row[5] = fm.format(anniversary);
            model.addRow(row);
        }
        
        
    }
